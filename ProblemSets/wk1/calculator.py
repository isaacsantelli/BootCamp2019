def mysum(arg1, arg2):
    '''
    This function takes two arguments and returns their sum
    Inputs: arg1, arg2 (scalars)
    Outputs: sum
    '''
    return arg1 + arg2

def myproduct(arg1, arg2):
    '''
    This function takes two arguments and returns their product
    Inputs: arg1, arg2 (scalars)
    Outputs: prpduct
    '''
    return arg1 * arg2
